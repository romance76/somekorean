total_questions = 0
total_answers = 0

for cat_slug, questions in qa_data.items():
    for title, content, answers in questions:
        uid = rand_user()
        has_reward = random.random() > 0.3
        reward = random.randint(10, 200) if has_reward else 0
        is_resolved = 1 if any(a[1] for a in answers) else (1 if random.random() < 0.6 else 0)
        views = random.randint(100, 5000)
        recommends = random.randint(0, 50)
        created = rand_date(12)

        cur.execute(f"""INSERT INTO qa_questions (user_id, category_slug, title, content, point_reward, is_resolved, view_count, answer_count, recommend_count, created_at, updated_at)
            VALUES ({uid}, '{esc(cat_slug)}', '{esc(title)}', '{esc(content)}', {reward}, {is_resolved}, {views}, {len(answers)}, {recommends}, '{created}', '{created}')""")
        qid = cur.lastrowid
        total_questions += 1

        # 추천 레코드
        rec_users = random.sample(USER_IDS, min(recommends, len(USER_IDS)))
        for ru in rec_users:
            try:
                cur.execute(f"INSERT INTO qa_question_recommends (question_id, user_id) VALUES ({qid}, {ru})")
            except:
                pass

        # 답변
        accepted_aid = None
        for ans_content, is_accepted in answers:
            au = rand_user()
            while au == uid:
                au = rand_user()
            al = random.randint(0, 30)
            ad = rand_date(10)
            cur.execute(f"""INSERT INTO qa_question_answers (question_id, user_id, content, is_accepted, like_count, created_at, updated_at)
                VALUES ({qid}, {au}, '{esc(ans_content)}', {1 if is_accepted else 0}, {al}, '{ad}', '{ad}')""")
            aid = cur.lastrowid
            total_answers += 1

            if is_accepted:
                accepted_aid = aid

            # 답변 좋아요
            al_users = random.sample(USER_IDS, min(al, len(USER_IDS)))
            for alu in al_users:
                try:
                    cur.execute(f"INSERT INTO qa_question_answer_likes (answer_id, user_id) VALUES ({aid}, {alu})")
                except:
                    pass

        # 채택된 답변 연결
        if accepted_aid and is_resolved:
            cur.execute(f"UPDATE qa_questions SET accepted_answer_id = {accepted_aid}, resolved_at = '{rand_date(8)}' WHERE id = {qid}")

    print(f"  {cat_slug}: {len(questions)} questions done")

