    private function mapExistingNews(): void
    {
        // 기존 뉴스의 category 문자열을 새 카테고리 ID에 매핑
        $mapping = [
            '이민/비자'   => 'immigration',
            '미국생활'    => 'life-leisure',
            '정치/사회'   => 'society-general',
            '경제'        => 'economy-general',
            '생활'        => 'life-leisure',
            '문화'        => 'culture-arts',
            '스포츠'      => 'sports-general',
            '기타'        => 'society-general',
        ];

        $categoryMap = NewsCategory::all()->keyBy('slug');

        foreach ($mapping as $oldCat => $newSlug) {
            $cat = $categoryMap->get($newSlug);
            if (!$cat) continue;

            $mainCat = $cat->parent_id
                ? $categoryMap->values()->firstWhere('id', $cat->parent_id)
                : $cat;

            News::where('category', $oldCat)
                ->whereNull('category_id')
                ->update([
                    'category_id'      => $cat->id,
                    'main_category_id' => $mainCat?->id,
                ]);
        }

        $this->command->info('기존 뉴스 카테고리 매핑 완료');
    }

