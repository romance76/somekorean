def rand_date_after(base_str, max_days=30):
    base = datetime.strptime(base_str, '%Y-%m-%d %H:%M:%S')
    delta = timedelta(hours=random.randint(1, max_days * 24))
    result = base + delta
    cap = datetime(2026, 4, 1)
    if result > cap:
        result = cap - timedelta(minutes=random.randint(1, 60))
    return result.strftime('%Y-%m-%d %H:%M:%S')

