def batch_insert(cursor, sql, rows):
    for i in range(0, len(rows), BATCH_SIZE):
        chunk = rows[i:i+BATCH_SIZE]
        cursor.executemany(sql, chunk)

def main():
    conn = mysql.connector.connect(**DB_CONFIG)
    cursor = conn.cursor()
    t0 = time.time()

    print("=" * 60)
    print("SomeKorean Seed Data Generator")
    print("=" * 60)

    # ── PART 1: Board Posts (6000+) ──
    print("\n[Part 1] Generating board posts...")
    posts_per_cat = 400
    all_posts = []
    post_meta = []  # (category, is_anonymous, created_at, like_count, comment_target)

    for cat_slug, cat_data in BOARD_CATEGORIES.items():
        titles = cat_data['titles']
        bodies = cat_data['bodies']
        is_anon_cat = (cat_slug == 'anonymous')

        for i in range(posts_per_cat):
            title_tpl = titles[i % len(titles)]
            title = title_tpl.replace('{N}', str(random.randint(1, 20)))
            if i >= len(titles):
                suffix_options = [" (2)", " 후기", " 이야기", " 공유", " 질문", " 고민", " 경험", " 추천", " 조언 구합니다", " 어떻게 생각하세요?"]
                title = title + random.choice(suffix_options)

            body = random.choice(bodies)
            body = body.replace('{N}', str(random.randint(1, 20)))
            created = rand_date(18)
            views = random.randint(10, 2000)
            likes = random.randint(0, 150)
            comment_target = random.randint(0, 12)
            is_anon = 1 if is_anon_cat else (1 if random.random() < 0.05 else 0)

            all_posts.append((
                rand_user(), cat_slug, title, body, is_anon,
                views, likes, comment_target, created, created
            ))
            post_meta.append((cat_slug, is_anon, created, likes, comment_target))

    post_sql = """INSERT INTO board_posts
        (user_id, category_slug, title, content, is_anonymous,
         view_count, like_count, comment_count, created_at, updated_at)
        VALUES (%s,%s,%s,%s,%s,%s,%s,%s,%s,%s)"""

    batch_insert(cursor, post_sql, all_posts)
    conn.commit()

    # Get inserted post IDs
    cursor.execute("SELECT id FROM board_posts ORDER BY id")
    post_ids = [r[0] for r in cursor.fetchall()]
    print(f"  Inserted {len(post_ids)} board posts")

    # ── PART 2: Board Comments ──
    print("\n[Part 2] Generating board comments...")
    all_comments = []
    post_comment_counts = {}

    for idx, pid in enumerate(post_ids):
        meta = post_meta[idx]
        comment_target = meta[4]
        is_anon_cat = (meta[0] == 'anonymous')
        post_created = meta[2]

        top_comments = max(0, comment_target - int(comment_target * 0.2))
        reply_count = comment_target - top_comments

        # Top-level comments
        top_ids_placeholder = []  # will be filled after insert
        for c in range(top_comments):
            content = random.choice(COMMENT_TEMPLATES)
            is_anon = 1 if is_anon_cat else (1 if random.random() < 0.05 else 0)
            c_date = rand_date_after(post_created, 60)
            all_comments.append((pid, rand_user(), None, content, is_anon, 0, c_date))

        # We'll handle replies in a second pass
        post_comment_counts[pid] = (top_comments, reply_count, post_created, is_anon_cat)

    comment_sql = """INSERT INTO board_comments
        (post_id, user_id, parent_id, content, is_anonymous, like_count, created_at)
        VALUES (%s,%s,%s,%s,%s,%s,%s)"""

    batch_insert(cursor, comment_sql, all_comments)
    conn.commit()

    # Now add replies (20% of comments are replies)
    print("  Adding reply comments...")
    cursor.execute("SELECT id, post_id FROM board_comments ORDER BY id")
    comment_rows = cursor.fetchall()
    comment_by_post = {}
    for cid, cpid in comment_rows:
        comment_by_post.setdefault(cpid, []).append(cid)

    reply_rows = []
    for pid, (top_c, reply_c, post_created, is_anon_cat) in post_comment_counts.items():
        if reply_c <= 0 or pid not in comment_by_post:
            continue
        parent_candidates = comment_by_post[pid]
        for r in range(reply_c):
            parent_id = random.choice(parent_candidates)
            content = random.choice(COMMENT_TEMPLATES)
            is_anon = 1 if is_anon_cat else (1 if random.random() < 0.05 else 0)
            c_date = rand_date_after(post_created, 60)
            reply_rows.append((pid, rand_user(), parent_id, content, is_anon, 0, c_date))

    if reply_rows:
        batch_insert(cursor, comment_sql, reply_rows)
        conn.commit()

    # Update actual comment_count
    cursor.execute("""UPDATE board_posts bp SET comment_count =
        (SELECT COUNT(*) FROM board_comments bc WHERE bc.post_id = bp.id)""")
    conn.commit()

    total_comments = len(all_comments) + len(reply_rows)
    print(f"  Inserted {total_comments} board comments")

    # ── PART 3: Board Post Likes ──
    print("\n[Part 3] Generating board post likes...")
    like_rows = []
