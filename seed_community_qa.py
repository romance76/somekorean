    conn.commit()

    cursor.execute("SELECT id FROM qa_questions ORDER BY id")
    question_ids = [r[0] for r in cursor.fetchall()]
    print(f"  Inserted {len(question_ids)} QA questions")

    # ── PART 5: QA Answers ──
    print("\n[Part 5] Generating QA answers...")
    all_answers = []
    answer_meta = []  # (question_id, is_best, like_count, q_user_id)

    for idx, qid in enumerate(question_ids):
        meta = q_meta[idx]
        is_resolved = meta[0]
        q_created = meta[2]
        answer_target = meta[3]
        q_user_id = all_questions[idx][0]

        if answer_target == 0:
            continue

        best_idx = random.randint(0, answer_target - 1) if is_resolved else -1

        for a in range(answer_target):
            detail = random.choice(ANSWER_DETAILS)
